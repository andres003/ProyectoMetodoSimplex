    private void BotonConfirmacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonConfirmacionActionPerformed
        validador = new Validador();
        validador.validarTabla(TablaDatos);
        validador.validarCampos(Campo1, Campo2);
        validador.validarGrupoBoton(MinimoMaximos);

        if (validador.isFlag()) {
            String lp_string = "" + Campo1.getText() + " " + Campo2.getText()
                    + " " + MinimoMaximos.getSelection().getActionCommand() + " . \n";

            String[][] matriz = validador.getMatriz();
            for (int i = 0; i < matriz.length; i++) {
                for (int j = 0; j < 4; j++) {
                    lp_string += matriz[i][j] + " ";
                }
                lp_string += "\n";
            }

            System.out.println(lp_string);

            InputString lp_input = new InputString(lp_string);
            try {
                lp_input.setInputFormat("X1:double, X2:double, TYPE:varstring(3), RHS:double");
                LP lp = new LP(lp_input);
                SolutionType solution_type = lp.resolve();
                if (solution_type == SolutionType.OPTIMUM) {
                    Variable[] vdd = new Variable[2];
                    double[] desicion = new double[2];
                    Solution solution = lp.getSolution();
                    vdd = solution.getVariables();
                    for (int i = 0; i < vdd.length; i++) {
                        desicion[i] = vdd[i].getValue();
                    }
                    DecimalFormat df = new DecimalFormat("#.000");
                    ResultadoX1.setText("" + df.format(desicion[0]));
                    ResultadoX2.setText("" + df.format(desicion[1]));
                    ResultadoFO.setText("" + df.format(solution.getOptimumValue()));
                    BotonReiniciar.setEnabled(true);
                    BotonConfirmacion.setEnabled(false);
                    JOptionPane.showMessageDialog(null, "SE HA ENCONTRADO UNA SOLUCION OPTIMA");
                } else {
                    JOptionPane.showMessageDialog(null, "NO SE HA ENCONTRADO UNA SOLUCION OPTIMA");
                }

            } catch (Exception ex) {
                Logger.getLogger(Ventana.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            JOptionPane.showMessageDialog(null, "¡INCONSISTENCIA EN LOS DATOS, POR FAVOR REVISAR!");
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_BotonConfirmacionActionPerformed

