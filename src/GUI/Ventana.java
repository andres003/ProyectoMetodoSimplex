    private void BotonReiniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonReiniciarActionPerformed
        Campo1.setText("");
        Campo2.setText("");
        MinimoMaximos.clearSelection();
        ComboDeRestricciones.setSelectedIndex(0);
        BotonReiniciar.setEnabled(false);
        BotonConfirmacion.setEnabled(true);
        ResultadoFO.setText("");
        ResultadoX1.setText("");
        ResultadoX2.setText("");
        DefaultTableModel temp = (DefaultTableModel) TablaDatos.getModel();
        int filas = TablaDatos.getRowCount();
        for (int a = 0; filas > a; a++) {
            temp.removeRow(0);
        }
        temp.setRowCount(1);
        TablaDatos.setModel(temp);

        // TODO add your handling code here:
    }//GEN-LAST:event_BotonReiniciarActionPerformed

